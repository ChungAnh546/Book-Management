    private void lbl_DangNhapMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lbl_DangNhapMouseClicked
        StringBuilder sb = new StringBuilder();
        DataValidator.validateEmptyTenDN(txt_TenDN, sb, "Tên đăng nhập không được để trống");
        DataValidator.validateEmptyMatKhau(txt_MatKhau, sb, "Mật khẫu không được để trống");
        
        if(sb.length()>0)
        {
            MessageDialogHelper.showErrorDialog(null, sb.toString(), "Lỗi");
            return;
        }
        try {
            String tenDN = txt_TenDN.getText();            
            String matKhau = new String(txt_MatKhau.getPassword());           
            TaiKhoanDao dao =new TaiKhoanDao();
            TaiKhoan tk = dao.checkLogin(tenDN, matKhau);
            if(tk==null)
            {
                JOptionPane.showMessageDialog(null, "Tài khoản hoặc mật khẫu không chính xác", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                //txt_TenDN.setForeground(Color.red);
                //txt_MatKhau.setForeground(Color.red);
            }else
            {
                SharedData.taiKhoanDN = tk;
                //Đăng nhập thành công
                this.dispose();
            }                                             
        } catch (Exception e) {
            e.printStackTrace();
            MessageDialogHelper.showErrorDialog( null, e.getMessage()
                , "Lỗi");
        }
    }//GEN-LAST:event_lbl_DangNhapMouseClicked

